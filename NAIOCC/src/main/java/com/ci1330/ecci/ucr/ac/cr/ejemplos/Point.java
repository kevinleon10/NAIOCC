package com.ci1330.ecci.ucr.ac.cr.ejemplos;

public class Point {

    private int x;
    private int y;

    public Point() {
        this.x = 12;
        this.y = 20;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
